package main

import (
	"gocycle/core"
)

var grids = loadGridsFromStrings(gridData)

func GetGrid(level int) [][]core.Square {
	if level < 0 || level >= len(grids) {
		level = 0
	}
	return grids[level]
}

func loadGridsFromStrings(gridData [][]string) [][][]core.Square {
	var grids [][][]core.Square

	for _, level := range gridData {
		height := len(level)
		if height == 0 {
			return nil
		}
		width := len(level[0])
		grid := make([][]core.Square, height)
		for y := range height {
			grid[y] = make([]core.Square, width)
			for x := range width {
				if level[y][x] == '#' {
					grid[y][x] = core.Wall
				} else {
					grid[y][x] = core.Open
				}
			}
		}
		grids = append(grids, grid)
	}
	return grids
}

